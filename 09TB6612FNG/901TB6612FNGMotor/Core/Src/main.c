/**
 * @brief Controla el motor derecho.
 * @param velocidad: de -100 (marcha atrás) a 100 (hacia adelante). 0 para frenar.
 */
#define MIN_FREQ 500
#define MAX_FREQ 20000
uint32_t map_esc_values(uint32_t au32_IN, uint32_t au32_INmin, uint32_t au32_INmax, uint32_t au32_OUTmin, uint32_t au32_OUTmax)
{
    return ((((au32_IN - au32_INmin)*(au32_OUTmax - au32_OUTmin))/(au32_INmax - au32_INmin)) + au32_OUTmin);
}

void motor_derecho(int velocidad)
{
  if (velocidad > 0) // Hacia adelante
  {
    HAL_GPIO_WritePin(GPIOB, GPIO_PIN_1, GPIO_PIN_SET);   // AIN1 HIGH
    HAL_GPIO_WritePin(GPIOB, GPIO_PIN_0, GPIO_PIN_RESET); // AIN2 LOW
  }
  else if (velocidad < 0) // Marcha atrás
  {
    HAL_GPIO_WritePin(GPIOB, GPIO_PIN_1, GPIO_PIN_RESET); // AIN1 LOW
    HAL_GPIO_WritePin(GPIOB, GPIO_PIN_0, GPIO_PIN_SET);   // AIN2 HIGH
  }
  else // Frenar
  {
    HAL_GPIO_WritePin(GPIOB, GPIO_PIN_1, GPIO_PIN_SET);   // AIN1 HIGH
    HAL_GPIO_WritePin(GPIOB, GPIO_PIN_0, GPIO_PIN_SET);   // AIN2 HIGH
  }

//   Calculamos el valor del pulso (0-99) basado en la velocidad (0-100)
  uint32_t pulso = map_esc_values(abs(velocidad), 0, 100, MIN_FREQ, MAX_FREQ);
  __HAL_TIM_SET_COMPARE(&htim4, TIM_CHANNEL_1, pulso);
//  TIM4->CCR1 = map_esc_values(velocidad, 0, 100, 0, 6545); // Map The ADC Reading To PWM DutyCycle
}

/**
 * @brief Controla el motor izquierdo.
 * @param velocidad: de -100 (marcha atrás) a 100 (hacia adelante). 0 para frenar.
 */
void motor_izquierdo(int velocidad)
{
  if (velocidad > 0) // Hacia adelante
  {
    HAL_GPIO_WritePin(GPIOB, GPIO_PIN_15, GPIO_PIN_SET);   // BIN1 HIGH
    HAL_GPIO_WritePin(GPIOB, GPIO_PIN_14, GPIO_PIN_RESET); // BIN2 LOW
  }
  else if (velocidad < 0) // Marcha atrás
  {
    HAL_GPIO_WritePin(GPIOB, GPIO_PIN_15, GPIO_PIN_RESET); // BIN1 LOW
    HAL_GPIO_WritePin(GPIOB, GPIO_PIN_14, GPIO_PIN_SET);   // BIN2 HIGH
  }
  else // Frenar
  {
    HAL_GPIO_WritePin(GPIOB, GPIO_PIN_15, GPIO_PIN_SET);   // BIN1 HIGH
    HAL_GPIO_WritePin(GPIOB, GPIO_PIN_14, GPIO_PIN_SET);   // BIN2 HIGH
  }

  uint32_t pulso = map_esc_values(abs(velocidad), 0, 100, MIN_FREQ, MAX_FREQ);
  __HAL_TIM_SET_COMPARE(&htim4, TIM_CHANNEL_2, pulso);
}
